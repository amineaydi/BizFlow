// ============================================================
// 🔧 Language Engine
// ============================================================

var LANG_META = {
    en: { flag: '🇬🇧', code: 'EN', dir: 'ltr', name: 'English'  },
    ar: { flag: '🇸🇦', code: 'AR', dir: 'rtl', name: 'العربية'  },
    fr: { flag: '🇫🇷', code: 'FR', dir: 'ltr', name: 'Français' }
};

// Get current language from localStorage
var currentLang = localStorage.getItem('bizflow_lang') || 'en';

// ============================================================
// t() - Translate a key
// Usage: t('save') → 'Save' / 'حفظ' / 'Enregistrer'
// ============================================================
function t(key) {
    return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) ||
           (TRANSLATIONS['en']        && TRANSLATIONS['en'][key])        ||
           key;
}

// ============================================================
// applyLanguage() - Switch language & update UI
// Usage: applyLanguage('ar')
// ============================================================
function applyLanguage(lang) {
    if (!TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem('bizflow_lang', lang);

    var meta = LANG_META[lang];

    // 1. Set HTML dir & lang
    document.documentElement.setAttribute('dir', meta.dir);
    document.documentElement.setAttribute('lang', lang);

    // 2. Update lang button UI
    var flagEl = document.getElementById('currentLangFlag');
    var codeEl = document.getElementById('currentLangCode');
    if (flagEl) flagEl.textContent = meta.flag;
    if (codeEl) codeEl.textContent = meta.code;

    // 3. Update active state in dropdown
    var options = document.querySelectorAll('[data-lang-switch]');
    for (var i = 0; i < options.length; i++) {
        if (options[i].dataset.langSwitch === lang) {
            options[i].classList.add('active');
        } else {
            options[i].classList.remove('active');
        }
    }

    // 4. Update all [data-lang] elements
    var elements = document.querySelectorAll('[data-lang]');
    for (var i = 0; i < elements.length; i++) {
        var key = elements[i].dataset.lang;
        var translation = t(key);
        if (translation) elements[i].textContent = translation;
    }

    // 5. Update all [data-lang-placeholder] elements
    var placeholders = document.querySelectorAll('[data-lang-placeholder]');
    for (var i = 0; i < placeholders.length; i++) {
        var key = placeholders[i].dataset.langPlaceholder;
        var translation = t(key);
        if (translation) placeholders[i].placeholder = translation;
    }

    // 6. Update all [data-lang-title] elements (tooltips)
    var titles = document.querySelectorAll('[data-lang-title]');
    for (var i = 0; i < titles.length; i++) {
        var key = titles[i].dataset.langTitle;
        var translation = t(key);
        if (translation) titles[i].title = translation;
    }

    // 7. Fire custom event so pages can hook in
    var event = new CustomEvent('langChanged', { detail: { lang: lang, dir: meta.dir } });
    document.dispatchEvent(event);

    // 8. Close dropdown
    var dropdown = document.getElementById('langDropdown');
    if (dropdown) dropdown.classList.remove('show');

    console.log('🌍 Language:', lang, '| Dir:', meta.dir);
}

// ============================================================
// initLangSwitcher() - Setup the language switcher button
// Call this in DOMContentLoaded on any page
// ============================================================
function initLangSwitcher() {

    // Toggle dropdown
    var toggle = document.getElementById('langToggle');
    if (toggle) {
        toggle.addEventListener('click', function(e) {
            e.stopPropagation();
            var dropdown = document.getElementById('langDropdown');
            if (dropdown) dropdown.classList.toggle('show');
        });
    }

    // Language options click
    var options = document.querySelectorAll('[data-lang-switch]');
    for (var i = 0; i < options.length; i++) {
        options[i].addEventListener('click', function() {
            applyLanguage(this.dataset.langSwitch);
            // If page has renderProducts, re-render
            if (typeof renderProducts === 'function') renderProducts();
            if (typeof renderCart     === 'function') renderCart();
            if (typeof renderPage     === 'function') renderPage();
        });
    }

    // Close dropdown on outside click
    document.addEventListener('click', function(e) {
        var dropdown = document.getElementById('langDropdown');
        var toggle   = document.getElementById('langToggle');
        if (dropdown && toggle) {
            if (!toggle.contains(e.target) && !dropdown.contains(e.target)) {
                dropdown.classList.remove('show');
            }
        }
    });

    // Apply saved language on load
    applyLanguage(currentLang);
}
